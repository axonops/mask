//go:build corpusgen

// Package main is the corpus fixture generator. It produces the
// `# === generated ===` section of every tests/corpus/<rule>.txt file
// from per-rule Generator implementations, then writes a .corpus.lock
// file mapping each file to its SHA-256.
//
//	make corpus-regen
//
// Determinism: every Generator uses a fixed math/rand seed (derived
// from the rule name) so output is byte-stable across machines and
// re-runs. mask.Apply computes the expected output, locking current
// library behaviour. Rule changes that alter generated fixtures must
// be reflected by re-running this tool and committing the diff.
package main

import (
	"bufio"
	"crypto/sha256"
	"encoding/hex"
	"fmt"
	"io"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"github.com/axonops/mask"
)

const (
	corpusDir       = "tests/corpus"
	canonicalBanner = "# === canonical (hand-written; preserved by generator) ==="
	generatedBanner = "# === generated (do not edit; regenerated by make corpus-regen) ==="
	lockFileName    = ".corpus.lock"
)

func main() {
	if err := run(); err != nil {
		fmt.Fprintf(os.Stderr, "corpus generator: %v\n", err)
		os.Exit(1)
	}
}

func run() error {
	files, err := filepath.Glob(filepath.Join(corpusDir, "*.txt"))
	if err != nil {
		return fmt.Errorf("glob: %w", err)
	}
	sort.Strings(files)

	for _, path := range files {
		rule := strings.TrimSuffix(filepath.Base(path), ".txt")
		if err := regenerateFile(path, rule); err != nil {
			return fmt.Errorf("%s: %w", path, err)
		}
	}

	if err := writeLockFile(files); err != nil {
		return fmt.Errorf("write %s: %w", lockFileName, err)
	}
	fmt.Printf("regenerated %d corpus files and %s\n", len(files), lockFileName)
	return nil
}

// regenerateFile preserves everything in path above the
// `# === generated ===` banner and rewrites everything below it from
// the registered generator (if any). Rules without a registered
// generator get the banner alone — the section stays empty.
func regenerateFile(path, rule string) error {
	canonical, err := readCanonicalSection(path)
	if err != nil {
		return err
	}

	var out strings.Builder
	out.WriteString(canonical)
	out.WriteString(generatedBanner)
	out.WriteByte('\n')

	if g, ok := generators[rule]; ok {
		if !mask.HasRule(rule) {
			return fmt.Errorf("generator registered for %q but rule is not", rule)
		}
		pairs := g.Generate()
		out.WriteString("#\n")
		out.WriteString(fmt.Sprintf("# %d generated fixtures for rule %q.\n", len(pairs), rule))
		out.WriteString("# Inputs come from a deterministic per-rule strategy; expected\n")
		out.WriteString("# outputs are mask.Apply at generation time.\n#\n")
		for _, p := range pairs {
			expected := mask.Apply(rule, p.Input)
			out.WriteString(p.Input)
			out.WriteByte('\t')
			out.WriteString(expected)
			out.WriteByte('\n')
		}
	}

	return os.WriteFile(path, []byte(out.String()), 0o644)
}

// readCanonicalSection returns the file content up to and including
// the line immediately before the `# === generated ===` banner. If the
// banner is absent, the entire file is treated as canonical and a new
// banner is appended after it.
func readCanonicalSection(path string) (string, error) {
	body, err := os.ReadFile(path)
	if err != nil {
		return "", err
	}
	idx := strings.Index(string(body), generatedBanner)
	if idx < 0 {
		// No banner — treat whole file as canonical and ensure it
		// ends with a newline.
		s := string(body)
		if !strings.HasSuffix(s, "\n") {
			s += "\n"
		}
		return s, nil
	}
	return string(body[:idx]), nil
}

// writeLockFile produces a sha256sum-compatible manifest at
// tests/corpus/.corpus.lock. Format: "<hex>  <basename>" per line.
func writeLockFile(files []string) error {
	sort.Strings(files)
	var b strings.Builder
	b.WriteString("# Auto-generated by `make corpus-regen`. Do not edit.\n")
	b.WriteString("# Format: <sha256-hex>  <filename> (two spaces, sha256sum-compatible).\n")
	for _, path := range files {
		sum, err := sha256File(path)
		if err != nil {
			return err
		}
		b.WriteString(sum)
		b.WriteString("  ")
		b.WriteString(filepath.Base(path))
		b.WriteByte('\n')
	}
	return os.WriteFile(filepath.Join(corpusDir, lockFileName), []byte(b.String()), 0o644)
}

func sha256File(path string) (string, error) {
	f, err := os.Open(path)
	if err != nil {
		return "", err
	}
	defer f.Close()
	h := sha256.New()
	if _, err := io.Copy(h, f); err != nil {
		return "", err
	}
	return hex.EncodeToString(h.Sum(nil)), nil
}

// uniqueLines is a small helper used by per-rule generators that want
// to dedup their input set before emitting. Returns lines in input
// order, preserving first occurrence.
func uniqueLines(in []string) []string {
	seen := make(map[string]struct{}, len(in))
	out := make([]string, 0, len(in))
	for _, s := range in {
		if _, ok := seen[s]; ok {
			continue
		}
		seen[s] = struct{}{}
		out = append(out, s)
	}
	return out
}

// silence unused-helper warnings when no generators reference these
// helpers yet — keeps `go vet` quiet during scaffolding.
